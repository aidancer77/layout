
function vol4_1() {
  const volume4_1 = document.getElementById('volume4_1');
  const checkVol4_1 = document.getElementById('checkVol4_1');

  if (checkVol4_1.checked == true) {
    volume4_1.classList.add('d-block');
    volume4_1.classList.remove('d-none');
    volume4_1.style.height="50%";
  } else {
    volume4_1.classList.add('d-none');
    volume4_1.classList.remove('d-block');
  }
}

