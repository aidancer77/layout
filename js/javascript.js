document.addEventListener('DOMContentLoaded', function () {
  const svgZoom4 = document.getElementById('svg_4_zoom');
  const groupZoom4 = document.getElementById('group_4_zoom');
  const groupHidden4 = document.getElementById('group_4_hidden');

  const svgZoom5 = document.getElementById('svg_5_zoom');
  const groupZoom5 = document.getElementById('group_5_zoom');
  const groupHidden5 = document.getElementById('group_5_hidden');

  let isExpanded = false;
  const h30Remove4 = document.getElementById('h-30_remove4');
  const h30Remove5 = document.getElementById('h-30_remove5');

  svgZoom4.addEventListener('click', function () {
    if (isExpanded) {
      // Возвращаем исходный размер и положение
      groupZoom4.classList.remove('zoomed');
      groupHidden4.classList.add('d-none');
      h30Remove5.style.removeProperty('height');
      h30Remove4.classList.add('height-30');
      h30Remove4.classList.remove('p-3');
    } 
    else {
      // Увеличиваем и центрируем
      groupZoom4.classList.add('zoomed');
      groupHidden4.classList.remove('d-none');
      h30Remove4.style.height = "100%"
      h30Remove4.classList.remove('height-30');
      h30Remove4.classList.add('p-3');
    }
    isExpanded = !isExpanded; // Переключаем состояние
  });

  svgZoom5.addEventListener('click', function () {
    if (isExpanded) {
      // Возвращаем исходный размер и положение
      groupZoom5.classList.remove('zoomed');
      groupHidden5.classList.add('d-none');
      h30Remove5.style.removeProperty('height');
      h30Remove5.classList.add('height-30');
      h30Remove5.classList.remove('pb-3');
      h30Remove5.classList.remove('ps-3');
    } else {
      // Увеличиваем и центрируем
      groupZoom5.classList.add('zoomed');
      groupHidden5.classList.remove('d-none');
      h30Remove5.style.height = "100%"
      h30Remove5.classList.remove('height-30');
      h30Remove5.classList.add('pb-3');
      h30Remove5.classList.add('ps-3');
    }
    isExpanded = !isExpanded; // Переключаем состояние
  });
});